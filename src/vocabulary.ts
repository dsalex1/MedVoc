const getDecU = (v: NounDec) => [getUNomSg(v), getUGenSg(v), getUNomPl(v), getUGenPl(v)];
const getDecE = (v: NounDec) => [getENomSg(v), getEGenSg(v), getENomPl(v), getEGenPl(v)];

const getDecAOA = (v: AdjDec, g: "m" | "f" | "n") => [getAONomSgA(v, g), getAOGenSgA(v, g), getAONomPlA(v, g), getAOGenPlA(v, g)];
const getDecKA = (v: AdjDec, g: "m" | "f" | "n") => [getKNomSgA(v, g), getKGenSgA(v, g), getKNomPlA(v, g), getKGenPlA(v, g)];
const getDecIA = (v: AdjDec, g: "m" | "f" | "n") => [getINomSgA(v, g), getIGenSgA(v, g), getINomPlA(v, g), getIGenPlA(v, g)];

export const getNounDec = (v: NounDec) => {
    if (v.dkl == "a") return getDecA(v);
    if (v.dkl == "o") return getDecO(v);
    if (v.dkl == "k") return getDecK(v);
    if (v.dkl == "uS") return getDecS(v);
    if (v.dkl == "iS") return getDecS(v);
    if (v.dkl == "i") return getDecI(v);
    if (v.dkl == "u") return getDecU(v);
    if (v.dkl == "e") return getDecE(v);
};
export const getAdjDec = (v: AdjDec) => {
    if (v.dkl == "ao") return [getDecAOA(v, "m"), getDecAOA(v, "f"), getDecAOA(v, "n")];
    if (v.dkl == "k") return [getDecKA(v, "m"), getDecKA(v, "f"), getDecKA(v, "n")];
    if (v.dkl == "i") return [getDecIA(v, "m"), getDecIA(v, "f"), getDecIA(v, "n")];
};

