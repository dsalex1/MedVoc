type NounDec = {
    typ: "N";
    dkl: "a" | "o" | "k" | "i" | "u" | "e" | "uS" | "iS";
    vocN: string;
    vocG: string;
    vocNP?: string;
    vocGP?: string;
    gen: "m" | "n" | "f";
    deu: string;
};
type AdjDec = { typ: "A"; dkl: "ao" | "k" | "i"; vocM: string; vocF?: string; vocN?: string; vocG?: string; deu: string };
type AdjSuffix = { typ: "aS"; voc: string; deu: string; bsp: string };
type Prefix = { typ: "P"; voc: string; deu: string };
type GreekPreSuff = { typ: "G"; voc: string; deu: string };
type GreekLatin = { typ: "GL"; vocG: string; vocL: string; deu: string };
type Misc = { typ: "M"; voc: string; deu: string };

const getANomSg = (v: NounDec) => v.vocN;
const getAGenSg = (v: NounDec) => v.vocN.slice(0, -1) + "ae";
const getANomPl = (v: NounDec) => v.vocN.slice(0, -1) + "ae";
const getAGenPl = (v: NounDec) => v.vocN.slice(0, -1) + "arum";

