const getAONomSgA = (v: AdjDec, g: "m" | "f" | "n") => v.vocM.slice(0, -2) + (g == "m" ? "us" : g == "f" ? "a" : "um");
const getAOGenSgA = (v: AdjDec, g: "m" | "f" | "n") => v.vocM.slice(0, -2) + (g == "m" ? "i" : g == "f" ? "ae" : "i");
const getAONomPlA = (v: AdjDec, g: "m" | "f" | "n") => v.vocM.slice(0, -2) + (g == "m" ? "i" : g == "f" ? "ae" : "a");
const getAOGenPlA = (v: AdjDec, g: "m" | "f" | "n") => v.vocM.slice(0, -2) + (g == "m" ? "orum" : g == "f" ? "arum" : "orum");

const getKNomSgA = (v: AdjDec, g: "m" | "f" | "n") => (g != "n" || !v.vocN ? v.vocM : v.vocN);
const getKGenSgA = (v: AdjDec, g: "m" | "f" | "n") => v.vocG;
